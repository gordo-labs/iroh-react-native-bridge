import { readFileSync, rmSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const here = dirname(fileURLToPath(import.meta.url));
const cmakePath = resolve(here, '../android/CMakeLists.txt');
const buildGradlePath = resolve(here, '../android/build.gradle');
const adapterPath = resolve(here, '../android/cpp-adapter.cpp');
const before = `execute_process(
    COMMAND node -p "require.resolve('uniffi-bindgen-react-native/package.json')"
    OUTPUT_VARIABLE UNIFFI_BINDGEN_PATH
    OUTPUT_STRIP_TRAILING_WHITESPACE
)
# Get the directory; get_filename_component and cmake_path will normalize
# paths with Windows path separators.
get_filename_component(UNIFFI_BINDGEN_PATH "\${UNIFFI_BINDGEN_PATH}" DIRECTORY)`;
const after = `execute_process(
    COMMAND node -p "require('path').resolve(require('path').dirname(require.resolve('uniffi-bindgen-react-native')), '../../..')"
    OUTPUT_VARIABLE UNIFFI_BINDGEN_PATH
    OUTPUT_STRIP_TRAILING_WHITESPACE
)`;

let contents = readFileSync(cmakePath, 'utf8');
if (!contents.includes(after) && !contents.includes(before)) {
  throw new Error(`Unexpected UBRN CMake format in ${cmakePath}`);
}
contents = contents.replace(before, after);
contents = contents.replace('project(ReactNativeIroh)', 'project(ReactNativeIrohBridge)');
contents = contents.replaceAll('gordo-labs-react-native-iroh', 'react-native-iroh');
contents = contents.replace(
  '    ../cpp/react-native-iroh.cpp\n    cpp-adapter.cpp',
  '    ../cpp/react-native-iroh.cpp\n    ../cpp/generated/iroh_mobile_bridge.cpp\n    cpp-adapter.cpp',
);
writeFileSync(cmakePath, contents);

let buildGradle = readFileSync(buildGradlePath, 'utf8');
buildGradle = buildGradle
  .replaceAll('project.properties["ReactNativeIroh_" + name]', 'project.properties["ReactNativeIrohBridge_" + name]')
  .replace('libraryName = "ReactNativeIroh"', 'libraryName = "IrohBridge"');
writeFileSync(buildGradlePath, buildGradle);

writeFileSync(
  adapterPath,
  `// Generated by uniffi-bindgen-react-native, with Iroh Android context wiring.
#include <jni.h>
#include <jsi/jsi.h>
#include <ReactCommon/CallInvokerHolder.h>
#include "react-native-iroh.h"

namespace jsi = facebook::jsi;
namespace react = facebook::react;

static jboolean initAndroidContext(
    JNIEnv *env,
    jobject applicationContext
) {
    JavaVM *javaVm = nullptr;
    if (env->GetJavaVM(&javaVm) != JNI_OK || javaVm == nullptr || applicationContext == nullptr) {
        return false;
    }

    jobject globalContext = env->NewGlobalRef(applicationContext);
    if (globalContext == nullptr) {
        return false;
    }

    uint8_t initialized = reactnativeiroh::initAndroidContext(
        reinterpret_cast<void *>(javaVm),
        reinterpret_cast<void *>(globalContext)
    );
    if (!initialized) {
        env->DeleteGlobalRef(globalContext);
    }
    return initialized != 0;
}

static jboolean installRustCrate(
    JNIEnv *env,
    jlong rtPtr,
    jobject callInvokerHolderJavaObj
) {
    using JCallInvokerHolder = facebook::react::CallInvokerHolder;

    auto holderLocal = facebook::jni::make_local(callInvokerHolderJavaObj);
    auto holderRef = facebook::jni::static_ref_cast<JCallInvokerHolder::javaobject>(holderLocal);
    auto* holderCxx = holderRef->cthis();
    auto jsCallInvoker = holderCxx->getCallInvoker();
    auto runtime = reinterpret_cast<jsi::Runtime *>(rtPtr);

    return reactnativeiroh::installRustCrate(*runtime, jsCallInvoker);
}

static jboolean cleanupRustCrate(jlong rtPtr) {
    auto runtime = reinterpret_cast<jsi::Runtime *>(rtPtr);
    return reactnativeiroh::cleanupRustCrate(*runtime);
}

extern "C"
JNIEXPORT jboolean JNICALL
Java_design_gordo_iroh_reactnative_IrohBridgeModule_nativeInitAndroidContext(
    JNIEnv *env,
    jobject type,
    jobject applicationContext
) {
    return initAndroidContext(env, applicationContext);
}

extern "C"
JNIEXPORT jboolean JNICALL
Java_design_gordo_iroh_reactnative_IrohBridgeModule_nativeInstallRustCrate(
    JNIEnv *env,
    jclass type,
    jlong rtPtr,
    jobject callInvokerHolderJavaObj
) {
    return installRustCrate(env, rtPtr, callInvokerHolderJavaObj);
}

extern "C"
JNIEXPORT jboolean JNICALL
Java_design_gordo_iroh_reactnative_IrohBridgeModule_nativeCleanupRustCrate(
    JNIEnv *env,
    jclass type,
    jlong rtPtr
) {
    return cleanupRustCrate(rtPtr);
}
`,
);

for (const generatedPath of [
  '../cpp/gordo-labs-react-native-iroh.cpp',
  '../cpp/gordo-labs-react-native-iroh.h',
  '../android/src/main/java/design/gordo/iroh/reactnative/ReactNativeIrohModule.kt',
  '../android/src/main/java/design/gordo/iroh/reactnative/ReactNativeIrohPackage.kt',
  '../src/NativeReactNativeIroh.ts',
  '../src/NativeReactNativeIroh.js',
  '../ReactNativeIroh.podspec',
  '../ios/ReactNativeIroh.h',
  '../ios/ReactNativeIroh.mm',
]) {
  rmSync(resolve(here, generatedPath), { force: true });
}
